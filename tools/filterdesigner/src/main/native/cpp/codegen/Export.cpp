// Prefixes each line of @a body with @a prefix, including the trailing line if
// it doesn't already end with a newline.
std::string PrefixLines(std::string_view body, std::string_view prefix) {
  std::string out;
  size_t pos = 0;
  while (pos < body.size()) {
    size_t nl = body.find('\n', pos);
    if (nl == std::string_view::npos) {
      nl = body.size();
    }
    out.append(prefix);
    out.append(body.substr(pos, nl - pos));
    out.push_back('\n');
    pos = nl + 1;
  }
  return out;
}

constexpr std::string_view kDoNotEdit =
    "Generated by WPILib Filter Designer.\n"
    "DO NOT EDIT \xE2\x80\x94 regenerate via the Filter Designer tool.\n";

std::string EmitCppFile(const Sections& sections, std::string_view className,
                        const ExportSpec& spec) {
  std::string out;
  out.append(PrefixLines(kDoNotEdit, "// "));
  out.append("//\n");
  out.append(PrefixLines(spec.description, "// "));
  out.append("\n");
  out.append("#pragma once\n\n");
  out.append("#include <wpi/math/filter/BiquadFilter.hpp>\n\n");
  out.append("namespace frc::filters {\n\n");
  out.append("inline wpi::math::BiquadFilter ");
  out.append(className);
  out.append("() {\n");
  out.append("  return wpi::math::BiquadFilter{\n");
  for (const Section& s : sections) {
    out.append("      {");
    out.append(FormatCoeff(s.b0));
    out.append(", ");
    out.append(FormatCoeff(s.b1));
    out.append(", ");
    out.append(FormatCoeff(s.b2));
    out.append(", ");
    out.append(FormatCoeff(s.a1));
    out.append(", ");
    out.append(FormatCoeff(s.a2));
    out.append("},\n");
  }
  out.append("  };\n");
  out.append("}\n\n");
  out.append("}  // namespace frc::filters\n");
  return out;
}

std::string EmitJavaFile(const Sections& sections, std::string_view className,
                         const ExportSpec& spec) {
  std::string out;
  out.append(PrefixLines(kDoNotEdit, "// "));
  out.append("//\n");
  out.append(PrefixLines(spec.description, "// "));
  out.append("\n");
  out.append("package frc.robot.filters;\n\n");
  out.append("import org.wpilib.math.filter.BiquadFilter;\n\n");
  out.append("public final class ");
  out.append(className);
  out.append(" {\n");
  out.append("  public static BiquadFilter create() {\n");
  out.append("    return new BiquadFilter(\n");
  for (size_t i = 0; i < sections.size(); ++i) {
    const Section& s = sections[i];
    out.append("        new BiquadFilter.Section(");
    out.append(FormatCoeff(s.b0));
    out.append(", ");
    out.append(FormatCoeff(s.b1));
    out.append(", ");
    out.append(FormatCoeff(s.b2));
    out.append(", ");
    out.append(FormatCoeff(s.a1));
    out.append(", ");
    out.append(FormatCoeff(s.a2));
    out.append(")");
    if (i + 1 < sections.size()) {
      out.push_back(',');
    }
    out.push_back('\n');
  }
  out.append("    );\n");
  out.append("  }\n\n");
  out.append("  private ");
  out.append(className);
  out.append("() {}\n");
  out.append("}\n");
  return out;
}

std::string EmitPythonFile(const Sections& sections, std::string_view className,
                           const ExportSpec& spec) {
  std::string snake = ToSnakeCase(className);
  std::string out;
  out.append(PrefixLines(kDoNotEdit, "# "));
  out.append("#\n");
  out.append(PrefixLines(spec.description, "# "));
  out.append("\n");
  out.append("from wpimath.filter import BiquadFilter\n\n\n");
  out.append("def ");
  out.append(snake);
  out.append("() -> BiquadFilter:\n");
  out.append("    return BiquadFilter([\n");
  for (const Section& s : sections) {
    out.append("        BiquadFilter.Section(b0=");
    out.append(FormatCoeff(s.b0));
    out.append(", b1=");
    out.append(FormatCoeff(s.b1));
    out.append(", b2=");
    out.append(FormatCoeff(s.b2));
    out.append(", a1=");
    out.append(FormatCoeff(s.a1));
    out.append(", a2=");
    out.append(FormatCoeff(s.a2));
    out.append("),\n");
  }
  out.append("    ])\n");
  return out;
}

